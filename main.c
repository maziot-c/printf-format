#include <stdint.h>
#include <stdio.h>

int main()
{
	int64_t t = 0;
	int32_t i = 0;

	printf("%-16s %-16s %-16s %-16s %-16s %-16s %-16s %-16s\n", "\%llu(64)", "\%lld(64)", "\%lu(64)", "\%ld(64)", "\%u(32)", "\%d(32)", "\%lu(32)", "\%ld(32)");
	printf("-----------------------------------------------------------------------------------------------------------------------------------\n");
	for (t = 0; t <= 10000000000; t += 1000000000) {
		i += 1000000000;
		printf("%-16llu %-16lld %-16lu %-16ld %-16u %-16d %-16lu %-16ld\n", t, t, t, t, i, i, i, i);
	}

	printf("\n");
	printf("%-16s %-16s %-16s %-16s %-16s %-16s %-16s %-16s\n", "\%llu(u64)", "\%lld(u64)", "\%lu(u64)", "\%ld(u64)", "\%u(u32)", "\%d(u32)", "\%lu(u32)", "\%ld(u32)");
	printf("-----------------------------------------------------------------------------------------------------------------------------------\n");
	for (t = 0, i= 0; t <= 10000000000; t += 1000000000) {
		i += 1000000000;
		printf("%-16llu %-16lld %-16lu %-16ld %-16u %-16d %-16lu %-16ld\n", (uint64_t)t, (uint64_t)t, (uint64_t)t, (uint64_t)t, (uint32_t)i, (uint32_t)i, (uint32_t)i, (uint32_t)i);
	}

	return 0;
}
